		<h3>Bio</h3>
			<p>Lee Van Arsdall currently resides in Jacksonville, Florida where he earned a BFA with a 
			concentration in photography from the University of North Florida. 
			He works as a Graphic Designer in Ponte Vedra Beach, Florida. 
			His current personal work deals with fabricating photographic images that question 
			the static notion of time in a photograph and the insistence that photographs are 
			transcriptions of reality. He also enjoys working with alternative cameras and printing 
			methods to create works that push the boundaries of traditional photographic techniques.
			</p>

